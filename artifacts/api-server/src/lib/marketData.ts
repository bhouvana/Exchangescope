export const enrichedCache = new Map<string, Quote>();
export function clearEnrichedCache() { enrichedCache.clear(); }
export function setEnrichedCache(s: string, q: Quote) { enrichedCache.set(s, q); }

export function getQuote(symbol: string): Quote | null {
  const row = getActiveSymbolMap()[symbol];
  if (!row) return null;
  const live = getLiveQuote(symbol);
  if (!live) return null;
  const cached = enrichedCache.get(symbol);
  return {
    symbol, name: live.name || row[0], sector: row[1],
    price: +live.price.toFixed(2), change: +live.change.toFixed(2),
    changePercent: +live.changePercent.toFixed(2), volume: live.volume,
    marketCap: live.marketCap > 0 ? live.marketCap : (cached?.marketCap ?? 0),
    peRatio: live.peRatio > 0 ? live.peRatio : (cached?.peRatio ?? 0),
    week52High: live.week52High > 0 ? live.week52High : (cached?.week52High ?? 0),
    week52Low: live.week52Low > 0 ? live.week52Low : (cached?.week52Low ?? 0),
    updatedAt: live.updatedAt,
  };
}

export function getAllQuotes(): Quote[] {
  return Object.keys(getActiveSymbolMap()).map(s => getQuote(s)).filter((q): q is Quote => q !== null);
}

export function enrichCompany(c: {
  symbol: string; name: string; exchange?: string; sector?: string | null;
  market_cap?: number | null; marketCap?: number | null;
}): Quote {
  // Accept both snake_case (from explicit DB select aliases) and camelCase (from Drizzle full-row select)
  const dbMarketCap = (c.market_cap ?? c.marketCap) ?? 0;
  const live = getLiveQuote(c.symbol);
  const cached = enrichedCache.get(c.symbol);
  if (live) {
    const found = findSymbolData(c.symbol);
    const sector = c.sector?.trim() || (found ? found.row[1] : "") || "Unknown";
    const cap = live.marketCap > 0 ? live.marketCap
      : (cached?.marketCap ?? 0) > 0 ? cached!.marketCap
      : dbMarketCap;
    return {
      symbol: c.symbol, name: live.name || c.name, sector,
      price: +live.price.toFixed(2), change: +live.change.toFixed(2),
      changePercent: +live.changePercent.toFixed(2), volume: live.volume,
      marketCap: cap,
      peRatio: live.peRatio > 0 ? live.peRatio : (cached?.peRatio ?? 0),
      week52High: live.week52High > 0 ? live.week52High : (cached?.week52High ?? 0),
      week52Low: live.week52Low > 0 ? live.week52Low : (cached?.week52Low ?? 0),
      updatedAt: live.updatedAt,
    };
  }
  if (cached) {
    return { ...cached, marketCap: cached.marketCap > 0 ? cached.marketCap : dbMarketCap };
  }
  const found = findSymbolData(c.symbol);
  const sector = c.sector?.trim() || (found ? found.row[1] : "") || "Unknown";
  const q: Quote = {
    symbol: c.symbol, name: c.name, sector,
    price: 0, change: 0, changePercent: 0, volume: 0,
    marketCap: dbMarketCap, peRatio: 0, week52High: 0, week52Low: 0,
    updatedAt: new Date().toISOString(),
  };
  enrichedCache.set(c.symbol, q);
  return q;
}

export async function getHistory(symbol: string, period: string, suffix?: string): Promise<OhlcBar[]> {
  try {
    const bars = suffix !== undefined
      ? await fetchLiveHistoryWithSuffix(symbol, period, suffix)
      : await fetchLiveHistory(symbol, period);
    if (bars.length > 0) return bars;
  } catch { /* fall through */ }
  return [];
}

