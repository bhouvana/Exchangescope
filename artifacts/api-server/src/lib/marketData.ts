let currentRegion: Region = "nasdaq";
let switchingLock = false;

export function getRegion(): Region { return currentRegion; }

export function getActiveSymbolMap(): Record<string, SymbolMeta> {
  if (currentRegion === "nasdaq") return SYMBOLS_NASDAQ;
  if (currentRegion === "nyse")   return SYMBOLS_NYSE;
  if (currentRegion === "nse")    return SYMBOLS_NSE;
  return SYMBOLS_BSE;
}
export function getActiveSymbolData() { return getActiveSymbolMap(); }

export function findSymbolData(symbol: string, restrictToRegion?: Region): { region: Region; row: SymbolMeta } | null {
  const maps: [Region, Record<string, SymbolMeta>][] = [
    ["nasdaq", SYMBOLS_NASDAQ], ["nyse", SYMBOLS_NYSE],
    ["nse", SYMBOLS_NSE],       ["bse", SYMBOLS_BSE],
  ];
  for (const [r, map] of maps) {
    if (restrictToRegion && r !== restrictToRegion) continue;
    if (map[symbol]) return { region: r, row: map[symbol] };
  }
  return null;
}

export function getRegionMeta() { return REGION_META[currentRegion]; }

export function setRegion(region: Region) {
  if (region === currentRegion || switchingLock) return;
  currentRegion = region;
  switchingLock = true;
  const suffixMap: Record<Region, string> = { nasdaq: "", nyse: "", nse: ".NS", bse: ".BO" };
  setYahooSuffix(suffixMap[region]);
  switchLiveSymbols(Object.keys(getActiveSymbolMap()), () => { switchingLock = false; });
  setTimeout(() => { switchingLock = false; }, 30000);
}

